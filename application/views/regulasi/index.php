    <h1>Regulasi Mengenai Subak</h1>
    <p>PERATURAN DAERAH PROVINSI BALI NOMOR 9 TAHUN 2012 TENTANG SUBAK</p>

    <pre>
        Dengan Persetujuan Bersama DEWAN PERWAKILAN RAKYAT DAERAH PROVINSI BALI dan GUBERNUR BALI
        MEMUTUSKAN:
        Menetapkan : PERATURAN DAERAH TENTANG SUBAK.

        BAB I
        KETENTUAN UMUM
        Pasal 1
        Dalam Peraturan Daerah ini, yang dimaksud dengan:
        1. Pemerintah Provinsi adalah Pemerintah Provinsi Bali.
        2. Gubernur adalah Gubernur Bali.
        3. Bupati/Walikota adalah Bupati/Walikota di Provinsi Bali.
        4. Subak adalah organisasi tradisional dibidang tata guna air dan atau tata tanaman di tingkat usaha tani pada masyarakat adat di Bali yang bersifat sosioagraris, religius, ekonomis yang secara historis terus tumbuh dan berkembang.
        5. Tempekan/Munduk/Banjaran/Arahan/Lanyahan adalah bagian dari subak yang membantu organisasi subak dalam tingkatan yang lebih kecil.
        6. Parhyangan Subak adalah tempat suci bagi krama subak dalam berhubungan dengan Ida Sanghyang Widhi.
        7. Pawongan Subak adalah hubungan antara krama subak.
        8. Palemahan Subak adalah wilayah subak sesuai dengan awig-awig subak.
        9. Pekaseh/Kelihan Subak adalah sebutan untuk Ketua Prajuru Subak.
        10. Majelis Subak adalah satuan organisasi yang dibentuk oleh para pekaseh dalam usaha memfasilitasi persoalanpersoalan dan pemberdayaan subak.
        11. Awig-awig Subak yang selanjutnya disebut Awig-awig adalah norma-norma adat yang disuratkan yang mengatur tentang Subak.
        12. Paruman adalah rapat-rapat yang dilaksanakan oleh krama subak.
        13. Krama Subak adalah orang yang menjadi anggota subak.

        BAB II
        ASAS DAN TUJUAN
        Pasal 2
        Subak berasaskan Pancasila, Undang-Undang Dasar Negara
        Republik Indonesia Tahun 1945 dan konsep Tri Hita Karana
        dijiwai Agama Hindu.
        Pasal 3
        Tujuan Subak mencakup :
        a. memelihara dan melestarikan organisasi subak;
        b. mensejahterakan kehidupan petani;
        c. mengatur pengairan dan tata tanaman;
        d. melindungi dan mengayomi petani; dan
        e. memelihara serta memperbaiki saluran air ke sawah.

        BAB III
        PALEMAHAN DAN KEANGGOTAAN SUBAK
        Pasal 4
        (1) Palemahan Subak merupakan wilayah subak tempat krama subak melakukan aktivitasnya sebagai petani dan mempunyai batas-batas tertentu.
        (2) Batas Palemahan Subak sebagaimana dimaksud pada ayat (1) ditetapkan oleh masing-masing Subak atas dasar permufakatan dengan Subak yang berbatasan.
        Pasal 5
        (1) Perubahan batas dan fungsi palemahan sebagaimana
        dimaksud dalam Pasal 4 dapat dilakukan berdasarkan
        kesepakatan krama subak, setelah mendapat persetujuan
        dari Bupati/Walikota yang bersangkutan.
        (2) Subak yang mampu mempertahankan palemahannya
        untuk tidak beralih fungsi dapat diberikan pengurangan,
        keringanan, atau pembebasan PBB oleh Kab/Kota.
        (3) Subak yang mampu mempertahankan palemahannya
        untuk tidak beralih fungsi dapat diberikan, program
        tambahan tertentu.
        (4) Ketentuan mengenai tata cara pengurangan keringanan
        atau pembebasan PBB diatur lebih lanjut oleh
        Bupati/Walikota.
        (5) Gubernur dapat memberikan bantuan keuangan/hibah
        kepada Subak.
        Pasal 6
        Ketentuan mengenai tata cara dan syarat menjadi krama
        subak berikut parahyangan dan palemahannya diatur
        dalam awig-awig.
        BAB IV
        KEDUDUKAN DAN FUNGSI SUBAK
        Pasal 7
        Kedudukan dan fungsi subak di Provinsi Bali sebagai
        organisasi tradisional yang mengayomi masyarakat adat Bali
        dibidang pertanian dan pengairan.
        Pasal 8
        Subak sebagai organisasi tradisional mempunyai fungsi:
        a. membantu pemerintah dalam meningkatkan
        pembangunan dibidang pertanian;
        b. melaksanakan hukum adat dan adat istiadat dalam
        subak;
        c. menetapkan awig-awig sebagai suatu kesepakatan dalam
        mengatur kepentingan sosial pertanian dan keagamaan;
        d. membina dan melestarikan nilai-nilai agama dan adatistiadat Bali serta tetap menjaga persatuan dan kesatuan
        anggota berdasarkan paras paros segilik seguluk
        selunglung sebayantaka;
        e. menjaga, memelihara, mengembangkan dan
        memanfaatkan kekayaan subak dan prasarana-prasarana
        irigasi lainnya guna menjamin kelancaran tertibnya irigasi
        dalam mewujudkan kesejahteraan masyarakat;
        f. mengembangkan kemampuan krama subak untuk
        meningkatkan produktifitas, pendapatan dan
        kesejahteraan petani; dan
        g. menjaga kelestarian wilayah subak dan lingkungannya
        dalam rangka pertanian berkelanjutan.
        BAB V
        AWIG-AWIG SUBAK
        Pasal 9
        (1) Setiap subak harus memiliki awig-awig.
        (2) Awig-awig sebagaimana dimaksud pada ayat (1) tidak
        boleh bertentangan dengan Pancasila, Undang-Undang
        Dasar Negara Republik Indonesia Tahun 1945 dan
        Peraturan Perundang-undangan.
        Pasal 10
        (1) Awig-awig sebagaimana dimaksud dalam Pasal 9 dibuat
        dan disahkan oleh krama subak.
        (2) Awig-awig sebagaimana dimaksud pada ayat (1)
        dicatatkan di Kantor Bupati/Walikota.
        Pasal 11
        Sanksi yang diatur dalam awig-awig tidak boleh
        bertentangan dengan peraturan perundang-undangan dan
        rasa keadilan dalam masyarakat.
        BAB VI
        PRAJURU SUBAK
        Pasal 12
        (1) Subak dipimpin oleh pekaseh/kelian subak.
        (2) Prajuru subak dipilih dan ditetapkan oleh krama subak.
        (3) Prajuru subak tidak boleh merangkap jabatan dalam
        struktur kepengurusan Subak bersangkutan.
        (4) Susunan prajuru subak dan tugas-tugasnya diatur dalam
        awig-awig.
        BAB VII
        PEMBINAAN DAN HUBUNGAN KERJA
        Pasal 13
        (1) Gubernur dalam melakukan pembinaan, fasilitasi,
        pemberdayaan subak, berkoordinasi dan bekerjasama
        dengan Bupati/Walikota.
        (2) Gubernur dalam melaksanakan fungsi sebagaimana
        dimaksud pada ayat (1) dibantu oleh instansi/lembaga
        teknis terkait.
        Pasal 14
        Hubungan kerja antara prajuru subak dengan Desa
        Dinas/Kelurahan, Desa Pakraman dan lembaga-lembaga
        lainnya bersifat koordinatif dan konsultatif.
        BAB VIII
        HARTA KEKAYAAN DAN PENDAPATAN SUBAK
        Pasal 15
        (1) Harta kekayaan subak mencakup harta kekayaan yang
        menjadi hak milik subak berupa harta tidak bergerak
        dan bergerak.
        (2) Pengelolaan harta kekayaan subak dilakukan oleh prajuru
        subak sesuai dengan awig-awig.
        (3) Penggunaan harta kekayaan subak diluar kepentingan
        subak harus disetujui berdasarkan keputusan paruman.
        Pasal 16
        (1) Pendapatan subak diperoleh dari:
        a.urunan, iuran, dan sawinih dari krama subak;
        b. pendapatan sendiri yang diperoleh dari hasil-hasil
        kekayaan subak;
        c.bantuan dari pemerintah; dan
        d. pendapatan lainnya yang sah dan tidak mengikat.
        (2) Besarnya urunan, iuran dan sawinih sebagaimana
        dimaksud pada ayat (1) huruf a ditetapkan dalam awigawig.
        Pasal 17
        (1) Subak berhak membentuk lembaga usaha ekonomi
        subak untuk meningkatkan pendapatan subak dan
        anggotanya.
        (2) Lembaga usaha yang dapat dikembangkan mencakup
        bidang yang berhubungan dengan kegiatan agribisnis di
        subak.
        (3) Tata cara penerimaan, pengelolaan dan penggunaan
        pendapatan subak sebagaimana dimaksud dalam
        Pasal 16 diatur melalui paruman, dengan sasaran untuk
        meningkatkan usaha-usaha produktif berlandaskan
        falsafah Tri Hita Karana.
        BAB IX
        TATA GUNA LAHAN, AIR DAN TANAMAN
        Pasal 18
        (1) Setiap penggunaan lahan pertanian harus berkoordinasi
        dengan pekaseh.
        (2) Krama subak yang akan melakukan alih fungsi lahan
        agar berkoordinasi dengan pekaseh dan mendapat
        persetujuan paruman subak.
        Pasal 19
        (1) Setiap pemanfaatan air bagi kepentingan di luar subak,
        harus berkoordinasi dengan seluruh krama subak.
        (2) Koordinasi sebagaimana dimaksud pada ayat (1),
        dilakukan melalui paruman.
        (3) Pemanfaatan air tanpa melalui koordinasi sebagaimana
        dimaksud pada ayat (2) tidak sah.
        (4) Setiap krama subak harus memelihara saluran-saluran
        air ditingkat usaha tani.
        (5) Setiap krama subak yang menemukan saluran air
        terganggu, harus melaporkan kepada prajuru subak.
        (6) Setiap orang yang dengan sengaja menghalangi/merusak
        saluran air dikenakan sanksi sesuai dengan awig-awig.
        Pasal 20
        (1) Setiap krama subak harus berkoordinasi tentang tata cara
        tanam di wilayah subaknya.
        (2) Tata tanaman atau jenis tanaman agar menyesuaikan
        dengan potensi lahan berdasarkan hasil penelitian dalam
        kerangka pembangunan ekonomi pertanian.
        BAB X
        MAJELIS SUBAK
        Pasal 21
        (1) Pekaseh/Kelian Subak dapat membentuk perkumpulan
        organisasi Pekaseh/Kelian Subak yang disebut Majelis
        Subak.
        (2) Majelis Subak digolongkan menjadi:
        a. Majelis Alit Subak untuk tingkat Kecamatan;
        b. Majelis Madya Subak untuk tingkat Kabupaten; dan
        c. Majelis Utama Subak untuk tingkat Provinsi.
        Pasal 22
        (1) Susunan Organisasi Majelis Subak pada masing-masing
        tingkatan meliputi:
        a. ketua;
        b. wakil ketua;
        c. sekretaris;
        d. bendahara;
        e. seksi parhyangan;
        f. seksi pawongan; dan
        g. seksi palemahan.
        (2) Pembentukan susunan organisasi sebagaimana
        dimaksud pada ayat (1), terdiri dari utusan prajuruprajuru dari masing-masing subak.
        Pasal 23
        Tugas Majelis Subak mencakup:
        a. sebagai mediator dalam penyelesaian sengketa yang terjadi
        antar subak pada masing-masing tingkatan; dan
        b. membantu penyuratan awig-awig.

