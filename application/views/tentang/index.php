    <h1>Apa Itu Subak?</h1>

    <pre style="">  

    Subak adalah organisasi kemasyarakatan yang khusus mengatur sistem pengairan sawah (irigasi) yang digunakan
    dalam bercocok tanam padi di Bali, Indonesia. Subak pada umumnya memiliki pura yang dinamakan Pura Uluncarik 
    atau Pura Bedugul, yang khusus dibangun oleh para pemilik lahan dan petani. Pura tersebut diperuntukkan bagi 
    Dewi Sri, yaitu dewi kemakmuran dan kesuburan menurut kepercayaan masyarakat Bali. Sistem irigasi ini diatur 
    oleh seorang pemuka adat (Pekaseh) yang juga adalah seorang petani di Bali.

    Sistem subak telah menjadi salah satu ciri khas dari masyarakat Bali. Sistem pengairan ini berkembang dalam 
    pengaruh nilai-nilai ajaran agama Hindu yang kuat dan membentuk suatu kearifan lokal, yang membuat masyarakat 
    petani di Bali dapat serasi dengan alam untuk memperoleh hasil panen yang optimal.
